                    # crop1 = im0s[int(xyxy[1]):int(xyxy[3]), int(xyxy[0]):int(xyxy[2])]
                    # cv2.imwrite('C:\\Users\\anill\\Desktop\\projects\\number_plate\\data\\cropd.jpg',crop1)
                    
