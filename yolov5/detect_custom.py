                    thresh = cv2.GaussianBlur(
                        thresh, (5, 5), 0)

                    img = thresh
                    h, w = img.shape[:2]  # get the actual images height and width
                    img = cv2.resize(img, (int(w*360/h), 360))
                    h, w = img.shape[:2]

                    # # gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)  # transform to grayscale
